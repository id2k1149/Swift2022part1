import Foundation

//: ## While циклы

/*:
 
    while condition {
        some code
    }
 
 */

var counter = 5

while counter > 0 {
    print("Hello!")
    counter -= 1
}


/*:
 
    repeat {
        some code
    } while condition
 
 */

repeat {
    print("Hello!")
    counter -= 1
} while counter > 0
